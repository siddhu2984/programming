public class type_conversion{
    public static void main(String[] args){
        int a = 10;
        double s = a;
        char c = 'j';
        int i = c;
        System.out.println(s);
        System.out.println(c);
        System.out.println(i);
        int m = (int)a;
        System.out.println(m);

    }
}